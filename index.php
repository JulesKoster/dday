                <h4>The Logistics</h4>
                </div>
                <p> The logistics behind the Normandy Campaign
                        Not for nothing did Vauban liken the art of warfare to the "art of survival". An army marches on its stomach and must be kept adequately supplied if it is to maintain its operational capacity - no easy task, especially with a fast moving front. This is where logistics comes in, or what Antoine Henri de Jomini, General of the French Empire, defined as "the practical art of moving armies"1.
                        It is no use building up stores of manufactured goods, raw materials, weapons, and so on, unless they can be brought to the right place at the right time.
                        
                        Following the attack on Pearl Harbor, the United States became one huge factory, entirely given over to the war effort. It was soon the Allies' main source of military equipment.
                        Some 3,500 cargo ships, most of them so-called "Liberty ships", were built to keep Europe supplied.
                        Britain saw a huge build-up of soldiers, weapons, goods and military equipment in the months, weeks and days before Operation Overlord, a gigantic seaborne invasion that the Allies hoped would establish a bridgehead on mainland Europe. This bridgehead would have to be adequately supplied, come what may.
                        In order to get the expeditionary forces to the French coast on D-Day, an exceptional logistics operation had to be mounted, codenamed Neptune. The invasion fleet was to comprise around 4,300 vessels of every shape, size and nationality. Once these arrived off the Normandy coast, they would disgorge the 2,600 landing barges intended to carry the assault troops as far as the beaches.
                        In the early hours of June 6th 1944, an armada of ships manned by 150,000 seamen transported 130,000 men and 20,000 vehicles across the Channel to open up a decisive breach in the Atlantic Wall.
                        
                        Operation Neptune, a logistical feat in itself, was now followed by a new and equally crucial phase: keeping the fragile bridgehead supplied so that it could withstand the inevitable German counter-attacks.
                        Although the Allies did not have a single port in Normandy on the evening of June 6th, they had had the ingenious idea of building two harbours completely from scratch, using components manufactured in England, towed across the Channel and assembled off the coast. This was Operation Mulberry.
                        No fewer than 40,000 people had worked on these different components in the run-up to D-Day.
                        Thus, as early as June 7th, the Royal Engineers set about putting this giant Meccano set together opposite Arromanches. Two weeks later, the port was fully operational and became a vital link in the Western Front's logistics chain.
                        Without this art of foresight - or logistics -, the Normandy Landings would certainly have ended in failure.
                        
                        1) "Logistics; or, the practical art of moving armies", the title of Chapter VI of The Art of War (1838), by Antoine Henri de Jomini, General of the French Empire.</p>
